# Getting Started

## Requirements
- PHP >= 5.6.4
- WordPress >= 4.9

## Installation
### WP-CLI
```shell
wp plugin install --activate <?= $data['downloadUrl'] ?>
```

### Composer
```shell
composer require joshmoreno/wpdev
```

### Manually
